package com.example.androidserviceexample.models;

public class Wind {

float speed;
float gust;
float deg;
/**
 * @return the speed
 */
public float getSpeed() {
	return speed;
}
/**
 * @param speed the speed to set
 */
public void setSpeed(float speed) {
	this.speed = speed;
}
/**
 * @return the gust
 */
public float getGust() {
	return gust;
}
/**
 * @param gust the gust to set
 */
public void setGust(float gust) {
	this.gust = gust;
}
/**
 * @return the deg
 */
public float getDeg() {
	return deg;
}
/**
 * @param deg the deg to set
 */
public void setDeg(float deg) {
	this.deg = deg;
}


}
