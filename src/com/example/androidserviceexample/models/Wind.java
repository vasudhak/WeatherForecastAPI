package com.example.androidserviceexample.models;

public class Wind {

int speed;
float gust;
int deg;
/**
 * @return the speed
 */
public int getSpeed() {
	return speed;
}
/**
 * @param speed the speed to set
 */
public void setSpeed(int speed) {
	this.speed = speed;
}
/**
 * @return the gust
 */
public float getGust() {
	return gust;
}
/**
 * @param gust the gust to set
 */
public void setGust(float gust) {
	this.gust = gust;
}
/**
 * @return the deg
 */
public int getDeg() {
	return deg;
}
/**
 * @param deg the deg to set
 */
public void setDeg(int deg) {
	this.deg = deg;
}


}
